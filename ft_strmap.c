#include "libft.h"

char	*ft_strmap(const char *s, char (*f)(char))
{
	char	*p;
	int		i;

	p = ft_strnew(ft_strlen(s));
	i = 0;
	while (s[i] != '\0' && p != NULL)
	{
		p[i] = f(s[i]);
		i++;
	}
	return (p);
}
